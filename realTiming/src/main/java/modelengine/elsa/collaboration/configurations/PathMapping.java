package modelengine.elsa.collaboration.configurations;

import modelengine.elsa.collaboration.interfaces.DataExchanger;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.core.Ordered;
import org.springframework.web.reactive.HandlerMapping;
import org.springframework.web.reactive.handler.SimpleUrlHandlerMapping;
import org.springframework.web.reactive.socket.WebSocketHandler;
import org.springframework.web.reactive.socket.server.support.WebSocketHandlerAdapter;

import java.util.HashMap;
import java.util.Map;

/**
 * websocket的路径映射。
 *
 * @author 陈镕希
 * @since 2025-07-29
 */
@Configuration
public class PathMapping {
    @Autowired
    @Bean
    public HandlerMapping webSocketMapping(DataExchanger dataExchanger) {
        final Map<String, WebSocketHandler> map = new HashMap<>();
        map.put("/collaboration/elsaData", dataExchanger);

        final SimpleUrlHandlerMapping mapping = new SimpleUrlHandlerMapping();
        mapping.setOrder(Ordered.HIGHEST_PRECEDENCE);
        mapping.setUrlMap(map);
        return mapping;
    }

    @Bean
    public WebSocketHandlerAdapter handlerAdapter() {
        return new WebSocketHandlerAdapter();
    }
}
